<li style="margin-bottom:20px;">-Şu şekilde,kendi qupoo's sayfanıza girdiğinizde qupoo'larınızın en sonunda bir qupoo ekleme butonu bulunur.Bu butonda iki seçeek vardır.URL ve KAŞİF seçenekleri istediğiniz herhangi bir siteyi eklemek için URL kısmına tıklayın ardından karşınıza gelen form da istediğiniz sitenin URL'sini kopyala yapıştır ile ve ya yazarak doldurun.Bir sonraki kutucuğa da sitenizi rahat bulabilmek için bir isim yazın.Butona tıkladığınızda siteniz qupoo's larınıza eklenmiş olacaktır...</li>
